// Тест проверяет, что операция сложения работает корректно.
// EXPECT_EQ проверяет, что выражение слева равно выражению справа.
// Если результат не совпадет, тест завершится с ошибкой.
#include <gtest/gtest.h>

// SampleTest - название группы тестов
// Addition - название теста
TEST(SampleTest, Addition) {
    // Проверяем, что 2 + 2 действительно равно 4
    EXPECT_EQ(2 + 2, 4);
}
